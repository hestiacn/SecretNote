
<b>Ⅰ. コアライセンス条項の改訂</b>
	<b>使用許可</b>
		ユーザーは、本システムを各种のシナリオに無料で適用できますが、すべての使用シナリオで元の著作権通知を顕著に保有する必要があります。これは、元の著者の貢献を表彰するためです。
		コアコードの無断転売は厳禁ですが、サービスホスティングの形で合理的な料金を請求するサービスを提供することは許可されています。
	<b>二次開発の基準</b>
		ユーザーは、本システムのコードを変更し、派生バージョンを開発する権限を明示的に与えられています。
		すべての変更版は、ウェブページ、ドキュメントの注釈、コンソールの出力など、本プロジェクトに基づいていることを顕著な位置に声明する必要があります。これは、元の著者の労働成果を尊重するためです。
	<b>特許の許可</b>
		本システムは、ユーザーに永久的な特許使用权を付与し、すべての含まれる特許技術をカバーします。
		ただし、インターフェースデザインの特許は、この許可の範囲内に含まれません。
