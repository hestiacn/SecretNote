四、修改 JSON 内容
您可以根据需要修改 JSON 中的内容：

    global：全局设置，包括页面标题、分类名称、搜索按钮文本、分页文本和页脚信息。
    index：首页设置，包括主标题、欢迎文本、快速查看选项、总留言数、搜索占位符、搜索选项和无结果时的提示信息。
    messages：留言设置，包括加密通知、密码输入框占位符、解锁按钮文本、密码提示、发布时间、内容标题、回复标题、按钮文本和用户信息。

五、保存配置

    完成修改后，点击页面底部的“保存配置”按钮。
    系统会自动创建备份并保存新的配置，保存成功后，您会看到提示信息。

六、查看效果

    保存配置后，访问留言本的首页，检查首页内容是否已更新。

注意事项

    确保 JSON 格式正确：错误的 JSON 格式可能导致页面显示异常。您可以使用在线 JSON 验证工具检查格式。
    备份文件：每次保存配置时，系统会自动创建备份。您可以在“数据备份”标签页中找到历史备份文件，并选择恢复到之前的版本。

常见问题
Q1：如果 JSON 格式错误怎么办？
A：系统在保存配置时会自动验证 JSON 格式。如果格式错误，系统会提示错误信息，并且不会保存错误的配置。您可以根据错误提示修正 JSON 格式。
Q2：如何恢复之前的配置？
A：每次保存配置时，系统会自动创建备份。您可以在“数据备份”标签页中找到历史备份文件，并选择恢复到之前的版本。