蓝宝石留言本首页内容修改教程
前言
本教程将指导您如何修改蓝宝石留言本的首页内容，包括欢迎消息、描述信息和最近的留言等。通过简单的步骤，您可以轻松自定义首页展示的内容，使其更符合您的需求和风格。
一、登录管理后台

    打开您的留言本网站，在浏览器地址栏输入网址，例如：https://your-domain.com/admin/adminlogin.php。
    在登录页面，输入您的管理员用户名和密码，然后点击“登录”按钮进入管理后台。

二、进入配置管理

    登录后，您会看到顶部导航栏，点击“系统配置”选项，这将带您进入配置管理页面。

三、修改首页内容配置

    在配置管理页面，您会看到多个标签页，点击“首页内容配置”标签页。
    在该标签页中，您会看到一个大型的文本区域，其中包含 JSON 格式的首页内容。您可以直接在文本区域内编辑 JSON 内容。

